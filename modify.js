async function downloadWithRetry(file, retries = 3) {
  for (let i = 0; i < retries; i++) {
    try {
      await download(
        `https://d1txs74qdv0iht.cloudfront.net/presets/${file}.json`,
        './presets'
      );
      console.log(`Downloaded: ${file}`);
      return true; // Return true if download is successful
    } catch (error) {
      console.error(
        `Failed to download ${file}, attempt ${i + 1}/${retries}: ${
          error.message
        }`
      );
      if (i === retries - 1) return false; // Return false if all retries failed
    }
  }
}

function isValidJsonStructure(json) {
  return json.body && Array.isArray(json.body.objects);
}

st3keyArray.map(async (key) => {
  let data;
  try {
    data = fs.readFileSync(`./presets/${key}.json`);
  } catch (err) {
    console.error(`Error reading ${key}.json: ${err.message}`);
    const downloaded = await downloadWithRetry(key);
    if (!downloaded) return; // If download fails after retries, skip this file
    data = fs.readFileSync(`./presets/${key}.json`); // Try reading the file again
  }

  try {
    const json = JSON.parse(data);
    if (!isValidJsonStructure(json)) {
      console.error(`Invalid JSON structure for ${key}`);
      const downloaded = await downloadWithRetry(key);
      if (!downloaded || !isValidJsonStructure(json)) return; // If download fails or JSON is still invalid, skip this file
    }

    json.body.objects.forEach((el) => {
      if (
        (el.type === 'group' || el.type === 'path') &&
        el.stroke &&
        el.fill === undefined
      ) {
        el.fill = null;
      }
    });

    const newJson = JSON.stringify(json);
    fs.writeFileSync(`./presets/${key}.json`, newJson);
    console.log(`Modification done for ${key}`);
  } catch (err) {
    console.error(`Error processing ${key}.json: ${err.message}`);
  }
});
